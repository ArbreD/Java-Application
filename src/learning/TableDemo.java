    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // Get position of selected employee row
        int pos = this.tbEmp.getSelectedRow();
        if (pos >= 0) {
            String code = (String) tbEmp.getValueAt(pos, 0);
            if (JOptionPane.showConfirmDialog(this, "Delete the " + code + " employee?") == JOptionPane.OK_OPTION)
            {
                data.remove(pos);
                tbEmp.updateUI();
                changed = true;
            }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnSaveToFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveToFileActionPerformed
        try {
            File f = new File(filename);
            PrintWriter pw = new PrintWriter(f);
            int n = this.tbEmp.getRowCount();
            for (int i =0; i < n; i++) {
                Vector<String> v = (Vector<String>) (data.get(i));
                String s = v.get(0) + "," + v.get(1) + "," + v.get(2);
                pw.println(s);
            }
            pw.close();
            changed = false;
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "The file " + filename + " not found.");
        }
    }//GEN-LAST:event_btnSaveToFileActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!valid()) return;
        int pos = tbEmp.getSelectedRow();
        String code = this.txtCode.getText().trim();
        String name = this.txtName.getText().trim();
        String salary = this.txtSalary.getText().trim();
        if (pos == -1 || addNew) {
            Vector v = new Vector();
            v.add(code);
            v.add(name);
            v.add(salary);
            data.add(v);
            addNew = false;
        } else {
            Vector v = (Vector) data.get(pos);
            v.set(1, name);
            v.set(2, salary);
        }
        tbEmp.updateUI();
        changed = true;
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        if (changed == true) {
            if (JOptionPane.showConfirmDialog(this, "Data changed. Save to file?\nY/N?", "Before Close", JOptionPane.OK_OPTION) == JOptionPane.OK_OPTION) {
                btnSaveToFileActionPerformed(null);
            }
        }
        System.exit(0);
    }//GEN-LAST:event_btnExitActionPerformed

    private void tbEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEmpMouseClicked
        int row = this.tbEmp.getSelectedRow();
        this.txtCode.setText((String) tbEmp.getValueAt(row, 0));
        this.txtCode.setEnabled(false); // User cannot update the code
        this.txtName.setText((String) tbEmp.getValueAt(row, 1));
        this.txtSalary.setText((String) tbEmp.getValueAt(row, 2));
        addNew = false; // view and update mode
    }//GEN-LAST:event_tbEmpMouseClicked

    private void tbEmpMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEmpMouseReleased
        // Turn off the on-table editting mode when user double-clicks on a cell
        // This is not good. The event may slower than user input
        if (this.tbEmp.isEditing()) {
            int row = this.tbEmp.getSelectedRow();
            int column = this.tbEmp.getSelectedColumn();
            tbEmp.getCellEditor(row, column).cancelCellEditing();
        }
    }//GEN-LAST:event_tbEmpMouseReleased

