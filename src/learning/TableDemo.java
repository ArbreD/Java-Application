    private void loadData() {
        try {
            File f = new File(filename);
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String aDetails;
            while ((aDetails = br.readLine()) != null) {
                StringTokenizer stk = new StringTokenizer(aDetails, ",");
                String code = stk.nextToken();
                String name = stk.nextToken();
                String salaryStr = stk.nextToken();
                Vector<String> v = new Vector<String>();
                v.add(code);
                v.add(name);
                v.add(salaryStr);
                data.add(v);
            }
            br.close();
            fr.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "The file " + filename + " not found.");
        }
    }

    private boolean valid() {
        String s = ""; // set the initial value to reset the error
        // checking the code
        if (addNew == true) {
            s = this.txtCode.getText().trim().toUpperCase();
            this.txtCode.setText(s);
            if (!s.matches("^E\\d(3)$")) // checking code format
            {
                JOptionPane.showMessageDialog(this, "Code format: E000");
                return false;
            }
            for (int i = 0; i < data.size(); i++) // checking duplicated
            {
                Vector v = (Vector) data.get(i);
                if (s.equals((String) v.get(0))) {
                    JOptionPane.showMessageDialog(this, "This codde is duplicated!");
                    txtCode.requestFocus();
                    return false;
                }
            }
        }
        // checking the name
        s = this.txtName.getText().trim();
        if (s.length() == 0) {
            JOptionPane.showMessageDialog(this, "Name is required");
            return false;
        }
        // checking the salary
        s = this.txtSalary.getText().trim();
        if (!s.matches("^\\d+$")) {
            JOptionPane.showMessageDialog(this, "Salary is required");
            return false;
        }
        return true;
    }

