    private void tbEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEmpMouseClicked
        int row = this.tbEmp.getSelectedRow();
        Employee emp = model.getRowData(row);
        this.txtCode.setText(emp.getCode());
        this.txtCode.setEnabled(false); // User cannot update the code
        this.txtName.setText(emp.getName());
        this.txtAddress.setText(emp.getAddress());
        this.txtSalary.setText(emp.getSalary() + "");
        boolean sex = emp.isSex();
        this.rbMale.setSelected(sex);
        this.rbFemale.setSelected(!sex);
        addNew = false; // view and update mode
    }//GEN-LAST:event_tbEmpMouseClicked

