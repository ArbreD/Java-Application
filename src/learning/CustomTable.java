    private void loadData() {
        try {
            FileReader fr = new FileReader(filename);
            BufferedReader br = new BufferedReader(fr);
            String s;
            while ((s = br.readLine()) != null) {
                StringTokenizer stk = new StringTokenizer(s, ";");
                String code = stk.nextToken();
                String name = stk.nextToken();
                String address = stk.nextToken();
                String sexStr = stk.nextToken();
                boolean sex = (sexStr.equalsIgnoreCase("male"));
                int salary = Integer.parseInt(stk.nextToken());
                model.addRow(new Employee(code, name, address, sex, salary));
            }
            br.close();
            fr.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

    private boolean valid() {
        String s = ""; // set the initial value to reset the error
        // checking the code
        if (addNew == true) {
            s = this.txtCode.getText().trim().toUpperCase();
            this.txtCode.setText(s);
//            if (!s.matches("^E\\d(3)$")) // checking code format
//            {
//                JOptionPane.showMessageDialog(this, "Code format: E000");
//                return false;
//            }
            for (int i = 0; i < model.getRowCount(); i++) // checking duplicated
            {
                Employee e = model.getRowData(i);
                if (s.equalsIgnoreCase(e.getCode())) {
                    JOptionPane.showMessageDialog(this, "This codde is duplicated!");
                    txtCode.requestFocus();
                    return false;
                }
            }
        }
        // checking the name
        s = this.txtName.getText().trim();
        if (s.length() == 0) {
            JOptionPane.showMessageDialog(this, "Name is required");
            return false;
        }
        // checking the salary
        s = this.txtAddress.getText().trim();
        if (s.length() == 0) {
            JOptionPane.showMessageDialog(this, "Address is required");
            return false;
        }
        s = this.txtSalary.getText().trim();
        if (!s.matches("^\\d+$")) {
            JOptionPane.showMessageDialog(this, "Salary is required");
            return false;
        }
        return true;
    }

