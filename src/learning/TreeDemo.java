    private void btSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSave1ActionPerformed
        // Save department details
        String code = this.txtDepCode.getText().trim().toUpperCase();
        txtDepCode.setText(code);
        String name = this.txtDepName.getText().trim();
        txtDepName.setText(name);
        if (addNewDep == true) {
            Department newDept = new Department(code, name);
            root.add(new DefaultMutableTreeNode(newDept));
        } else {
            curDepNode.setUserObject(new Department(code, name));
        }
        this.tree.updateUI();
        this.addNewDep = false;
        this.curDepNode = this.curEmpNode = null;
    }//GEN-LAST:event_btSave1ActionPerformed

