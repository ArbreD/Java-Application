    private void loadData() {
        String s = "";
        StringTokenizer stk;
        try {
            FileReader fr = new FileReader(filename);
            BufferedReader br = new BufferedReader(fr);
            while ((s = br.readLine()) != null) {
                s = s.trim();
                boolean isDept = (s.charAt(s.length() - 1) == ':');
                stk = new StringTokenizer(s, "-:,");
                String code = stk.nextToken().trim();
                String name = stk.nextToken().trim();
                if (isDept) {
                    curDepNode = new DefaultMutableTreeNode(new Department(code, name));
                    root.add(curDepNode);
                } else {
                    int salary = Integer.parseInt(stk.nextToken().trim());
                    curEmpNode = new DefaultMutableTreeNode(new Employee2(code, name, salary));
                    curDepNode.add(curEmpNode);
                }
            }
            br.close();
            fr.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void viewDeptAndEmp() {
        Department curDept = null;
        Employee2 curEmp = null;
        if (curDepNode != null) {
            curDept = (Department) curDepNode.getUserObject();
        }
        if (curEmpNode != null) {
            curEmp = (Employee2) curEmpNode.getUserObject();
        }
        this.txtDepCode.setText(curDept != null ? curDept.getDepCode() : "");
        this.txtDepName.setText(curDept != null ? curDept.getDepName() : "");
        this.txtEmpCode.setText(curEmp != null ? curEmp.getEmpCode() : "");
        this.txtEmpName.setText(curEmp != null ? curEmp.getEmpName() : "");
        this.txtSalary.setText(curEmp != null ? "" + (curEmp.getSalary()) : "");
        txtDepCode.setEditable(false);
        txtEmpCode.setEditable(false);
    }

